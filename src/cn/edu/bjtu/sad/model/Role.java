package cn.edu.bjtu.sad.model;

public class Role {
	private int role_id;
	private int role_power;
	private String role_describe;
	public int getRole_id() {
		return role_id;
	}
	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}
	public int getRole_power() {
		return role_power;
	}
	public void setRole_power(int role_power) {
		this.role_power = role_power;
	}
	public String getRole_describe() {
		return role_describe;
	}
	public void setRole_describe(String role_describe) {
		this.role_describe = role_describe;
	}
	
}
